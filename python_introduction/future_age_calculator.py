# a Python script that asks the user for their current age and then calculates how old they will be in a specific future year.

# Prompt the user for their current age
current_age = int(input("How old are you? "))

# Calculate age in 2050 (2050 - 2023 = 27 years later)
age_in_2050 = current_age + 27

# Print the result
print(f"In 2050, you will be {age_in_2050} years old.")

